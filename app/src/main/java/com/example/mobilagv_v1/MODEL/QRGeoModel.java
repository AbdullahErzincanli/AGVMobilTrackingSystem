package com.example.mobilagv_v1.MODEL;

public class QRGeoModel {
    private String lat,lng,geo_plave;

    public QRGeoModel() {
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getGeo_plave() {
        return geo_plave;
    }

    public void setGeo_plave(String geo_plave) {
        this.geo_plave = geo_plave;
    }
}
